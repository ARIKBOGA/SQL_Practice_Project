-- GET ME FIRST_NAME, LAST_NAME, DEPARTMENT_NAME, CITY, COUNTRY_NAME AND REGION_NAME FOR ALL EMPLOYEES
SELECT FIRST_NAME, LAST_NAME, DEPARTMENT_NAME, CITY, COUNTRY_NAME, REGION_NAME
FROM EMPLOYEES E
         LEFT JOIN DEPARTMENTS D ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
         LEFT JOIN LOCATIONS L ON D.LOCATION_ID = L.LOCATION_ID
         LEFT JOIN COUNTRIES C2 on C2.COUNTRY_ID = L.COUNTRY_ID
         LEFT JOIN REGIONS R on C2.REGION_ID = R.REGION_ID;